package com.hillel.hometask;

public class Calc {
    public static int add(int x, int y) {
        int result = x + y;
        return result;
    }
    public static int sub(int x, int y) {
        int resul = x - y;
        return resul;
    }
    public static int div(int x, int y) {
        int resu = x / y;
        return resu;
    }
    public static int mul(int x, int y) {
        int res = x * y;
        return res;
    }
}
